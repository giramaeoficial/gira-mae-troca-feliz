import React, { useState, useCallback, useRef, useEffect } from 'react';
import { Button } from '@/components/ui/button';
import { Camera, X, Upload, Image } from 'lucide-react';
import { cn } from '@/lib/utils';
import { cropToSquare } from '@/utils/imageCompression';
import { toast } from '@/hooks/use-toast';

interface ImageUploadProps {
  value: File[];
  onChange: (files: File[]) => void;
  maxFiles?: number;
  maxSizeKB?: number;
  accept?: string;
  className?: string;
  disabled?: boolean;
}

const ImageUpload: React.FC<ImageUploadProps> = ({
  value = [],
  onChange,
  maxFiles = 3,
  maxSizeKB = 5000,
  accept = "image/*",
  className,
  disabled = false
}) => {
  const [previews, setPreviews] = useState<string[]>([]);
  const [isUploading, setIsUploading] = useState(false);
  const [isDragOver, setIsDragOver] = useState(false);
  const fileInputRef = useRef<HTMLInputElement>(null);

  const generatePreviews = useCallback((files: File[]) => {
    // Limpar previews antigos
    previews.forEach(url => URL.revokeObjectURL(url));
    
    const newPreviews = files.map(file => URL.createObjectURL(file));
    setPreviews(newPreviews);
  }, [previews]);

  const processFiles = async (files: FileList | File[]) => {
    const fileArray = Array.from(files);
    const remainingSlots = maxFiles - value.length;
    const filesToProcess = fileArray.slice(0, remainingSlots);

    console.log('üîÑ Processando', filesToProcess.length, 'arquivos...');

    const validFiles: File[] = [];
    
    for (const file of filesToProcess) {
      if (!file.type.startsWith('image/')) {
        toast({
          title: "Tipo de arquivo inv√°lido",
          description: `${file.name} n√£o √© uma imagem v√°lida`,
          variant: "destructive"
        });
        continue;
      }

      if (file.size > maxSizeKB * 1024) {
        toast({
          title: "Arquivo muito grande",
          description: `${file.name} excede o limite de ${maxSizeKB}KB`,
          variant: "destructive"
        });
        continue;
      }

      validFiles.push(file);
    }

    if (validFiles.length === 0) return;

    setIsUploading(true);
    try {
      console.log('üì∏ Processando', validFiles.length, 'imagens em formato quadrado...');
      
      // MUDAN√áA PRINCIPAL: Usar cropToSquare em vez de compressImage
      const compressedFiles = await Promise.all(
        validFiles.map(async (file) => {
          try {
            return await cropToSquare(file, 1024, 0.85);
          } catch (error) {
            console.error('Erro ao processar imagem:', error);
            return file;
          }
        })
      );

      const newFiles = [...value, ...compressedFiles];
      onChange(newFiles);
      generatePreviews(newFiles);

      toast({
        title: "Imagens adicionadas",
        description: `${compressedFiles.length} imagem(ns) processada(s) em formato quadrado`,
      });
    } catch (error) {
      console.error('Erro no processamento das imagens:', error);
      toast({
        title: "Erro no processamento",
        description: "Falha ao processar as imagens",
        variant: "destructive"
      });
    } finally {
      setIsUploading(false);
    }
  };

  const handleFileSelect = async (event: React.ChangeEvent<HTMLInputElement>) => {
    const files = event.target.files;
    if (!files) return;
    await processFiles(files);
    event.target.value = '';
  };

  const handleDragOver = (e: React.DragEvent) => {
    e.preventDefault();
    e.stopPropagation();
    if (!disabled && !isUploading) {
      setIsDragOver(true);
    }
  };

  const handleDragLeave = (e: React.DragEvent) => {
    e.preventDefault();
    e.stopPropagation();
    setIsDragOver(false);
  };

  const handleDrop = async (e: React.DragEvent) => {
    e.preventDefault();
    e.stopPropagation();
    setIsDragOver(false);
    
    if (disabled || isUploading) return;
    
    const files = e.dataTransfer.files;
    if (files.length > 0) {
      await processFiles(files);
    }
  };

  const removeImage = (indexToRemove: number) => {
    const newFiles = value.filter((_, index) => index !== indexToRemove);
    onChange(newFiles);
    
    const newPreviews = previews.filter((_, index) => index !== indexToRemove);
    URL.revokeObjectURL(previews[indexToRemove]);
    setPreviews(newPreviews);
  };

  const openFileDialog = () => {
    fileInputRef.current?.click();
  };

  // Atualizar previews quando value mudar
  React.useEffect(() => {
    if (value.length > 0) {
      generatePreviews(value);
    }
  }, [value]);

  // Cleanup previews on unmount
  React.useEffect(() => {
    return () => {
      previews.forEach(url => URL.revokeObjectURL(url));
    };
  }, []);

  return (
    <div className={cn('space-y-4', className)}>
      {/* Preview das imagens */}
      {previews.length > 0 && (
        <div className="grid grid-cols-3 gap-4">
          {previews.map((preview, index) => (
            <div key={index} className="relative group">
              <img 
                src={preview} 
                alt={`Preview ${index + 1}`}
                className="w-full h-24 object-cover rounded-lg border-2 border-gray-200"
              />
              <button
                type="button"
                onClick={() => removeImage(index)}
                disabled={disabled}
                className="absolute -top-2 -right-2 bg-red-500 text-white rounded-full w-6 h-6 flex items-center justify-center text-xs hover:bg-red-600 transition-colors opacity-0 group-hover:opacity-100"
              >
                <X className="w-3 h-3" />
              </button>
              {index === 0 && (
                <div className="absolute top-1 left-1 bg-blue-500 text-white text-xs px-1 rounded">
                  Principal
                </div>
              )}
            </div>
          ))}
        </div>
      )}

      {/* Dica de formato quadrado */}
      {value.length === 0 && (
        <div className="mb-4 p-3 bg-blue-50 rounded-lg border border-blue-200">
          <div className="flex items-start gap-2">
            <Camera className="w-5 h-5 text-blue-600 flex-shrink-0 mt-0.5" />
            <div>
              <p className="text-sm font-medium text-blue-900">
                üì∏ Dica: Centralize o item na foto
              </p>
              <p className="text-xs text-blue-700 mt-1">
                Suas fotos ser√£o automaticamente ajustadas para formato quadrado (como no Instagram). 
                O item ser√° centralizado automaticamente.
              </p>
            </div>
          </div>
        </div>
      )}

      {/* Upload area */}
      {value.length < maxFiles && (
        <div
          className={cn(
            "border-2 border-dashed rounded-lg p-6 text-center transition-all duration-200 cursor-pointer",
            isDragOver 
              ? "border-primary bg-primary/5 scale-105" 
              : "border-gray-300 hover:border-gray-400",
            (disabled || isUploading) && "cursor-not-allowed opacity-50"
          )}
          onDragOver={handleDragOver}
          onDragLeave={handleDragLeave}
          onDrop={handleDrop}
          onClick={openFileDialog}
        >
          <input
            ref={fileInputRef}
            type="file"
            accept={accept}
            multiple
            onChange={handleFileSelect}
            disabled={disabled || isUploading}
            className="hidden"
          />
          
          <div className="flex flex-col items-center gap-3">
            {isUploading ? (
              <Upload className="w-10 h-10 text-primary animate-bounce" />
            ) : isDragOver ? (
              <Image className="w-10 h-10 text-primary" />
            ) : (
              <Camera className="w-10 h-10 text-gray-400" />
            )}
            
            <div className="space-y-1">
              <p className={cn(
                "font-medium",
                isDragOver ? "text-primary" : "text-gray-700"
              )}>
                {isUploading ? 'Processando imagens...' : 
                 isDragOver ? 'Solte as imagens aqui' : 
                 'Clique ou arraste fotos aqui'}
              </p>
              <p className="text-sm text-gray-500">
                {value.length}/{maxFiles} fotos ‚Ä¢ Max {maxSizeKB}KB cada
              </p>
              <p className="text-xs text-gray-400">
                JPG, PNG, WebP at√© {maxSizeKB}KB
              </p>
            </div>
          </div>
        </div>
      )}
    </div>
  );
};

export default ImageUpload;
